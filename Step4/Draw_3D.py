import os
import numpy as np
import matplotlib.pyplot as plt
from mpl_toolkits.mplot3d import Axes3D

def Draw_Graph():
	
	fig = plt.figure()
	ax = fig.add_subplot(111, projection='3d')

	# Draw graph

	# Name Label
	ax.set_xlabel('X : Latitude')
	ax.set_ylabel('Y : Longitude')
	ax.set_zlabel('Z : minute')

	plt.show()

if __name__ == '__main__':
	Draw_Graph()
